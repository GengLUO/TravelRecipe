package be.kuleuven.travelrecipe.models;

public class SmallArea {
    private String name1;
    private int number1;
    private int picture1;
    private String name2;
    private int number2;
    private int picture2;
    private String name3;
    private int number3;
    private int picture3;


    public SmallArea(String name1, int number1, int picture1, String name2, int number2, int picture2, String name3, int number3, int picture3) {
        this.name1 = name1;
        this.number1 = number1;
        this.picture1 = picture1;
        this.name2 = name2;
        this.number2 = number2;
        this.picture2 = picture2;
        this.name3 = name3;
        this.number3 = number3;
        this.picture3 = picture3;
    }

    public String getName1() {
        return name1;
    }

    public void setName1(String name1) {
        this.name1 = name1;
    }

    public int getNumber1() {
        return number1;
    }

    public void setNumber1(int number1) {
        this.number1 = number1;
    }

    public int getPicture1() {
        return picture1;
    }

    public void setPicture1(int picture1) {
        this.picture1 = picture1;
    }

    public String getName2() {
        return name2;
    }

    public void setName2(String name2) {
        this.name2 = name2;
    }

    public int getNumber2() {
        return number2;
    }

    public void setNumber2(int number2) {
        this.number2 = number2;
    }

    public int getPicture2() {
        return picture2;
    }

    public void setPicture2(int picture2) {
        this.picture2 = picture2;
    }

    public String getName3() {
        return name3;
    }

    public void setName3(String name3) {
        this.name3 = name3;
    }

    public int getNumber3() {
        return number3;
    }

    public void setNumber3(int number3) {
        this.number3 = number3;
    }

    public int getPicture3() {
        return picture3;
    }

    public void setPicture3(int picture3) {
        this.picture3 = picture3;
    }
}
